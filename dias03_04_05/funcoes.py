def soma(x:int | float, y: int | float) -> int | float:
    """Soma x + y e retorna o resultado"""
    return x + y

def print_sub(x: int|float, y: int|float) -> None:
    """Recebe dois valores e printa o resultado da subtração entre eles"""
    print(f"{x-y}")

